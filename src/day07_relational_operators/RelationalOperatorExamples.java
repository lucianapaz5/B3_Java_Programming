package day07_relational_operators;

public class RelationalOperatorExamples {

    public static void main(String[] args) {

        System.out.println(10 > 5);
        System.out.println(10 < 5);

        int a = 5;
        int b = 3;

        System.out.println(a > b);
        boolean aAndb = a < b;
        System.out.println(aAndb);

    }
}
